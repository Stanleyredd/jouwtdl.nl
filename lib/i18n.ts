export function translateRuntimeMessage(message: string, language: AppLanguage) {
  if (
    message.startsWith(
      "Your Supabase journal schema is missing the required unique constraints",
    )
  ) {
    return language === "nl"
      ? "Je Supabase-journalschema mist de unieke constraints die nodig zijn om journal-entries op te slaan. Voer de nieuwste journal-migratie uit en vernieuw de app."
      : message;
  }

  if (message.startsWith("Your Supabase journal schema is missing ")) {
    return language === "nl"
      ? "Je Supabase-journalschema mist vereiste tabellen of kolommen. Voer de nieuwste journal-migratie uit en vernieuw de app."
      : message;
  }

  if (
    message.startsWith(
      "Your Supabase journal tables are missing the required grants or RLS permissions.",
    )
  ) {
    return language === "nl"
      ? "Je Supabase-journaltabellen missen de juiste grants of RLS-permissies. Voer de nieuwste journal-migratie uit en vernieuw de app."
      : message;
  }

  return runtimeMessageTranslations[message]?.[language] ?? message;
}

export function joinSentenceList(items: string[], language: AppLanguage) {
  if (items.length === 0) {
    return "";
  }

  if (items.length === 1) {
    return items[0];
  }

  if (items.length === 2) {
    return language === "nl"
      ? `${items[0]} en ${items[1]}`
      : `${items[0]} and ${items[1]}`;
  }

  return language === "nl"
    ? `${items.slice(0, -1).join(", ")}, en ${items.at(-1)}`
    : `${items.slice(0, -1).join(", ")}, and ${items.at(-1)}`;
}

export function normalizeLanguage(value: string | null | undefined): AppLanguage {
  return value === "en" ? "en" : "nl";
}
